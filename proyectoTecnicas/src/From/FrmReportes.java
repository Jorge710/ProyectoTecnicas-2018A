    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            conexion con = new conexion();
            Connection conn = con.getConexion();

            JasperReport reporte = null;
            String path = "src\\reporte\\reporteFactura.jasper";
            reporte = (JasperReport) JRLoader.loadObjectFromLocation(path);

            //para hacer reportes por busquedas de parametros.
            //tomamos del texfield si es un String hay q convertirle a int con Integer.parseInt()
            Map parametro = new HashMap();
            parametro.put("cedCli", Integer.parseInt(txtRucCli_factura.getText()));
            JasperPrint jprint = JasperFillManager.fillReport(reporte, parametro, conn);

            JasperViewer view = new JasperViewer(jprint, false);

            view.setDefaultCloseOperation(DISPOSE_ON_CLOSE);

            view.setVisible(true);
        } catch (JRException ex) {
            Logger.getLogger(FrmReportes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

